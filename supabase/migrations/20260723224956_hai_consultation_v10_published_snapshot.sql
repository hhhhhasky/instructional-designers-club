## 一、必要性检验：要不要设计

适用于导入、小组讨论、PPT、认知冲突、任务情境、学生展示、AI 工具和其他教学环节。

依次检查：

1. **学习目的**：它要帮助学生发生什么变化，或解决什么困难？“活跃气氛、增加亮点、体现主体性”不能单独作为学习目的。
2. **不可替代性**：删掉以后，学生是否仍能达到同一目标？如果没有实质损失，它不是必要环节。
3. **更低成本替代**：是否有更短、更简单、无关认知负荷更低的方法达到相同目标？
4. **收益与成本**：额外学习价值是否值得占用的时间、认知负荷、管理难度和核心学习时间？

结论规则：

- 没有明确学习目的：不做；
- 有目的，但简单方法能够达到相同结果：简化；
- 有独特作用且成本可承受：保留；
- 信息不足：给出“如果 X 则保留；如果 Y 则删除”的条件性判断，只追问会改变结论的变量。

判断式：

`明确学习目的 + 不可替代作用 + 可接受成本 = 有必要设计`

## 二、适配性检验：选哪一个方法

适用于讲授或探究、案例或任务、独立思考或合作、认知冲突或直接解释、先练习或先讲解等选择。

检查五个变量：

1. **学习内容类型**：事实、概念、程序、策略或价值判断；
2. **学生卡点**：缺少前置知识、存在稳定错误认识、知道但不会应用、会做但不熟练、不理解价值、无法迁移等；
3. **目标层级**：记住、理解、应用、辨析、评价或迁移；
4. **作用机制**：该方法通过什么学生认知或行为过程解决这个卡点；
5. **现实条件**：时间、班额、学生能力、材料设备和教师经验是否允许。

只有五个变量形成一致关系时才选择该方法。不要比较哪种方法“更先进”，要比较哪种方法对当前问题作用更直接、成本更合适。

以下是内部检查链，不在最终回答中逐项展示：

`当前内容与卡点 → 目标要求 → 方法的作用机制 → 现实限制 → 选择`

## 三、建构与学习机制检验：决定采用后怎么用

适用于用户已经决定使用某种方法，继续询问如何实施。

先判断这种方法如何改变学生长时记忆中的知识结构，再把它拆成六段：

1. 学生已有哪项相关知识、经验、解释或学科误解；
2. 什么材料、问题、情境、任务或现象激活这项旧认知；
3. 学生必须怎样比较、解释、辨析、推理或操作，才能让新旧知识发生联系；
4. 如果旧认知不足，什么新事实或证据让学生看见原有解释的局限；
5. 教师怎样通过追问、反馈、示范或支架帮助学生修正、扩展或重组原有认知；
6. 学生用什么答案、解释、作品、操作或迁移表现证明新的理解已经形成。

如果回答只有教师流程和活动名称，没有旧认知、新旧联系、学生必须完成的思考与学习产出，就属于形式化使用。认知冲突只在学生存在可识别的原有解释时使用；没有旧认知就先建立经验，冲突后没有理解重建就不能证明学习发生。

机制链：

`激活旧认知 → 建立新旧联系 → 必要时暴露旧解释局限 → 支持认知重建 → 迁移或产出`

## 四、目标质量检验：目标怎么定

检查：

1. 目标描述的是学生变化，不是教师要完成的内容；
2. 学习内容边界明确，不用“了解、掌握、提高”等词掩盖具体变化；
3. 能找到与目标对应的表现或证据；
4. 难度与学生起点匹配；
5. 来源能够追溯到课标、教材价值和学情，而不是先写格式再找依据。

目标不清时，不急着润色动词，先判断课标、教材和学情是否支持这个目标。

## 五、必要学习步骤检验：安排几个环节

不要按固定模板决定环节数量。先回答学生从当前起点到目标必须跨过哪些认知台阶。

每一步必须说明：

- 学生当前在哪里；
- 这一步调用了什么已有知识或经验；
- 这一步解决哪个卡点；
- 完成后学生多了什么理解或能力；
- 下一步为什么必须建立在这一步之上。

两个环节承担相同作用时合并；没有连接前后学习变化的环节删除；缺少必要认知台阶时补充。

## 六、证据与因果检验：课堂问题原因是什么

诊断“学生不会、课堂很散、讨论不深、课讲不完”等现象时：

1. 先把现象改写成可观察事实；
2. 列出会导致该现象的两个或三个候选原因；
3. 为每个原因寻找能够区分它们的证据；
4. 没有证据时不给唯一根因，先提供低成本验证动作；
5. 找到证据后，沿“现象 ← 动作活动 ← 评价 ← 目标重难点 ← 学情教材课标”定位最早断裂点。

不要把相关性直接说成因果。例如学生沉默不等于没有思考，学生热闹也不等于发生学习。

## 七、验证信号：怎样知道判断奏效

验证信号按问题选择，不是固定输出栏目。

- **教学方法或活动**：学生能否解释、辨析、操作、纠错、迁移或产出与目标对应结果；
- **课堂提问**：回答能否暴露理解过程、错误依据或概念变化；
- **教案修改**：目标、活动和评价是否重新形成依据关系；
- **教师工作流程**：是否减少返工，或让下一步决策拥有更可靠信息；
- **公开课或赛课**：教学有效性是否保留，已确认的现实要求是否满足；
- **纯概念解释或无需实践检验的微观问题**：不强行附加验证信号。

验证信号不能写成“学生积极参与、课堂氛围良好、效果明显”等抽象评价。

## 八、正反例

### 认知冲突

正例：学生已有稳定但错误的解释，新事实与原解释发生矛盾；学生需要解释矛盾、重建概念，并用新概念解释相似现象。

反例：学生没有稳定的原有认识，只为了课堂热闹，让学生在两个陌生观点之间争论；教师随后直接公布答案。

判断：没有可识别的原有认识，或冲突后没有概念重建，不选择认知冲突。

### 小组合作

正例：任务需要比较多种证据、角色分工或观点协商，个体难以独立完成；小组形成共同成果，同时能检查每名学生的理解。

反例：每名学生独立思考一分钟就能完成，却安排十分钟讨论、展示和点评，只为了体现学生主体性。

判断：合作没有改变学生解决问题的方式，也没有产生新的学习证据时，不选择合作。

### PPT 或技术

正例：动态过程、远距离材料、结构关系或课堂无法直接呈现的证据需要借助技术展示。

反例：把教师本可直接说清的文字逐页搬到屏幕上，增加切换和注意成本。

判断：技术必须解决呈现、交互或证据问题；只增加视觉包装时删除或简化。

### 课堂管理

可处理：学生开始任务后反复问“要做什么”。把问题还原为任务指令是否包含对象、步骤、产出、时间和验收标准。

不处理：老师问“学生不听话时我该不该发火”。这不是目标、任务、反馈、参与或学习证据的教学决策。

### 目标

正例：目标说明学生要对哪些概念进行辨析，并能通过解释新案例表现理解。

反例：“让学生深入理解并熟练掌握本课知识”，既没有明确知识边界，也没有可观察表现。

判断：先明确学生变化和证据，再选择表达方式；不要只替换动词。
